## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# vximg completion                                         -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/dannyben/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_vximg_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    echo "$words"'
  echo $'  '
  echo $'  else'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" != "-" ]] && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $''
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_vximg_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=("${COMP_WORDS[@]:1:$COMP_CWORD-1}")'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  case "$compline" in'
  echo $'    \'upscale\'*\'--factor\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "4k 8k")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'upscale\'*\'--model\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "realesr-animevideov3 realesrgan-x4plus realesrgan-x4plus-anime realesrnet-x4plus")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "--help -h")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'upscale\'*\'-m\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "realesr-animevideov3 realesrgan-x4plus realesrgan-x4plus-anime realesrnet-x4plus")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*\'--factor\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "4k 8k")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'upscale\'*\'-f\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "4k 8k")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*\'--model\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "realesr-animevideov3 realesrgan-x4plus realesrgan-x4plus-anime realesrnet-x4plus")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'upscale\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "--factor --help --model --upscaler-binary --upscaler-path -b -f -h -m -u")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*\'-m\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "realesr-animevideov3 realesrgan-x4plus realesrgan-x4plus-anime realesrnet-x4plus")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*\'-f\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "4k 8k")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "--factor --help --model --upscaler-binary --upscaler-path -b -f -h -m -u")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_vximg_completions_filter "--help --version -h -v completions u upscale")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'complete -F _vximg_completions vximg'
  echo $''
  echo $'# ex: filetype=sh'
}